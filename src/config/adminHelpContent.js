/**
 * @param {string} pageId
 * @param {string} [tabId] — מזהה טאב: AdminTheme → THEME_TAB_HELP; AdminSiteContent → SITE_CONTENT_TAB_HELP
 */
export function getAdminPageHelp(pageId, tabId) {
    if (pageId === 'theme') {
        if (tabId && THEME_TAB_HELP[tabId]) {
            return THEME_TAB_HELP[tabId];
        }
        return THEME_TAB_HELP.primaryColor;
    }
    if (pageId === 'site-content') {
        if (tabId && SITE_CONTENT_TAB_HELP[tabId]) {
            return SITE_CONTENT_TAB_HELP[tabId];
        }
        return SITE_CONTENT_TAB_HELP['hero-content'];
    }
    return ADMIN_HELP_CONTENT[pageId] || null;
}
